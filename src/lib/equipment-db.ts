/**
 * Search the equipment database by query string with optional category filter.
 * Searches across model, manufacturer, type, and capacity fields (case-insensitive).
 * Results are sorted by relevance: exact model matches first, then partial matches.
 */
export function searchEquipment(
  query: string,
  category?: string,
): EquipmentSpec[] {
  const q = query.toLowerCase().trim();
  if (!q) return category ? EQUIPMENT_DATABASE.filter((e) => e.category === category) : [...EQUIPMENT_DATABASE];

  const allEquipment = getAllEquipment();

  // Filter by category first if provided
  const pool = category
    ? allEquipment.filter((e) => e.category === category)
    : allEquipment;

  // Score each item for relevance
  const scored = pool
    .map((equipment) => {
      let score = 0;
      const model = equipment.model.toLowerCase();
      const manufacturer = equipment.manufacturer.toLowerCase();
      const type = equipment.type.toLowerCase();
      const capacity = equipment.capacity.toLowerCase();

      // Exact model match — highest priority
      if (model === q) {
        score += 100;
      } else if (model.startsWith(q)) {
        score += 80;
      } else if (model.includes(q)) {
        score += 60;
      }

      // Manufacturer match
      if (manufacturer === q) {
        score += 50;
      } else if (manufacturer.includes(q)) {
        score += 30;
      }

      // Type match
      if (type === q) {
        score += 40;
      } else if (type.includes(q)) {
        score += 20;
      }

      // Capacity match
      if (capacity === q) {
        score += 40;
      } else if (capacity.includes(q)) {
        score += 20;
      }

      return { equipment, score };
    })
    .filter((item) => item.score > 0);

  // Sort by score descending
  scored.sort((a, b) => b.score - a.score);

  return scored.map((item) => item.equipment);
}

