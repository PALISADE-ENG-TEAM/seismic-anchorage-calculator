// Custom Equipment (localStorage)
// ============================================================================

const CUSTOM_EQUIPMENT_KEY = 'custom_equipment';

/**
 * Retrieve custom equipment entries from localStorage.
 */
export function getCustomEquipment(): EquipmentSpec[] {
  if (typeof window === 'undefined') return [];
  try {
    const stored = localStorage.getItem(CUSTOM_EQUIPMENT_KEY);
    if (!stored) return [];
    const parsed = JSON.parse(stored);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

/**
 * Save a custom equipment entry to localStorage.
 * Automatically sets `isCustom: true` on the entry.
 */
export function saveCustomEquipment(equipment: EquipmentSpec): void {
  if (typeof window === 'undefined') return;
  const existing = getCustomEquipment();
  const updated = [...existing, { ...equipment, isCustom: true }];
  localStorage.setItem(CUSTOM_EQUIPMENT_KEY, JSON.stringify(updated));
}

/**
 * Delete a custom equipment entry from localStorage by ID.
 */
export function deleteCustomEquipment(id: string): void {
  if (typeof window === 'undefined') return;
  const existing = getCustomEquipment();
  const updated = existing.filter((e) => e.id !== id);
  localStorage.setItem(CUSTOM_EQUIPMENT_KEY, JSON.stringify(updated));
}

/**
 * Get all equipment: built-in database combined with user's custom entries.
 * Custom equipment is appended after the built-in database.
 */
export function getAllEquipment(): EquipmentSpec[] {
  return [...EQUIPMENT_DATABASE, ...getCustomEquipment()];
}
